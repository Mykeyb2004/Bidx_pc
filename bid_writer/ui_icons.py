def _bitmap_to_xbm(rows: tuple[str, ...]) -> str:
    rendered_rows: list[str] = []
    for row in rows:
        left = int(row[:8][::-1], 2)
        right = int(row[8:][::-1], 2)
        rendered_rows.append(f" 0x{left:02x}, 0x{right:02x}")
    data = ",\n".join(rendered_rows)
    return (
        "#define bid_writer_icon_width 16\n"
        "#define bid_writer_icon_height 16\n"
        "static unsigned char bid_writer_icon_bits[] = {\n"
        f"{data}\n"
        "};"
    )


def _image_cache(owner: object) -> dict[tuple[str, str], tk.BitmapImage]:
    cache = getattr(owner, "_bid_writer_icon_cache", None)
    if cache is None:
        cache = {}
        setattr(owner, "_bid_writer_icon_cache", cache)
    return cache


def _remember_image(owner: object, image: object) -> None:
    images = getattr(owner, "_bid_writer_icon_images", None)
    if images is None:
        images = []
        setattr(owner, "_bid_writer_icon_images", images)
    if image not in images:
        images.append(image)


def get_icon_image(owner: object, name: str, *, foreground: str = "#2f3a45") -> tk.BitmapImage | None:
    """Return a cached 16x16 bitmap image, or None if Tk cannot create it."""
    if name not in ICON_NAMES:
        return None

    rows = _BITMAP_DATA.get(name)
    if rows is None:
        return None

    cache = _image_cache(owner)
    key = (name, foreground)
    if key not in cache:
        try:
            cache[key] = tk.BitmapImage(data=_bitmap_to_xbm(rows), foreground=foreground)
        except (RuntimeError, tk.TclError):
            return None
    return cache[key]


def configure_icon_button(
    button: tk.Misc,
    owner: object,
    icon_name: str,
    *,
    foreground: str = "#2f3a45",
) -> bool:
    """Attach an icon to a ttk button while preserving a text fallback."""
    image = get_icon_image(owner, icon_name, foreground=foreground)
    if image is None:
        return False
    try:
        button.configure(image=image, compound="left")
    except (AttributeError, TypeError, tk.TclError):
        return False
    _remember_image(button, image)
    return True


def add_icon_menu_command(
    menu: tk.Menu,
    *,
    label: str,
    command: Callable[[], object],
    icon_name: str,
    owner: object,
    foreground: str = "#2f3a45",
) -> None:
    """Add a menu command with an image when supported, otherwise plain text."""
    image = get_icon_image(owner, icon_name, foreground=foreground)
    if image is None:
        menu.add_command(label=label, command=command)
        return

    try:
        menu.add_command(label=label, command=command, image=image, compound="left")
    except TypeError:
        menu.add_command(label=label, command=command)
        return
    except tk.TclError:
        menu.add_command(label=label, command=command)
        return
    _remember_image(menu, image)
